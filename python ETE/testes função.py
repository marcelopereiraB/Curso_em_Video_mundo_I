def cor_azul(str):
    print(f'\033[34m{str}')


def cor_vermelha(str):
    print(f'\033[31m{str}')


def cor_amarela(str):
    print(f'\033[33m{str}')


def cor_roxa(str):
    print(f'\033[35m{str}')


cor_roxa('marcelo pereira barbosa da silva')
cor_azul('marcelo pereira barbosa da silva')
cor_amarela('marcelo pereira barbosa da silva')
